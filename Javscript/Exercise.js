// Khai báo biến name và gán giá trị kiểu string
var name = "John";

// Khai báo biến isStudent và gán giá trị kiểu boolean
var isStudent = true;

// In giá trị của cả hai biến ra màn hình
console.log("Tên của bạn là: " + name);
console.log("Bạn là sinh viên: " + isStudent);

// Khai báo biến num1 và num2 và gán giá trị số
var num1 = 10;
var num2 = 5;

// Thực hiện các phép tính và in kết quả ra màn hình
